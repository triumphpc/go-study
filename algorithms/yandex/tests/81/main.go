package main

import (
	"bufio"
	"fmt"
	"io"
	"os"
)

func main() {
	task(os.Stdin, os.Stdout)
}

func task(in io.Reader, out io.Writer) {
	// 1. –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤ —Ö—ç—â —Å—Ç—Ä–æ–∫–∏ –±–æ–ª—å—à–∏–µ 1 - –∫–∞–∂–¥—É—é –ø–æ–¥—Å—Ç—Ä–æ–∫—É
	// 2. –ï—Å–ª–∏ –≤ –º–∞–ø–µ –µ—Å—Ç—å —Ç–µ–∫—É—â–∞—è+1, –ø—Ä–æ–≤–µ—Ä—è–µ–º –µ–∑–µ –ø–ª—é—Å 1 –∏ —Ç–¥ –∏ –ø–æ–¥—Å—á–∏—Ç—ã–≤–∞–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –æ–ø–µ—Ä–∞—Ü–∏–π

	scanner := bufio.NewScanner(in)
	scanner.Split(bufio.ScanWords)

	writer := bufio.NewWriter(out)
	defer func() {
		_, _ = writer.WriteString("\n")
		_ = writer.Flush()
	}()

	//res := make(map[string][]int)
	scanner.Scan()
	world := scanner.Text()

	chrs := make(map[string]bool, len(world))
	chrs[world[0:2]] = true // –ü–µ—Ä–≤—É—é –ø–æ–¥—Å—Ç—Ä–æ–∫—É —Å—Ä–∞–∑–∑—É –ø–æ–ª–æ–∂–∏–º –≤ –∫—ç—â
	cnt := 2 + len(world)%2 // –ü–µ—Ä–≤—ã–µ –¥–≤–∞ —Å–∏–º–≤–æ–ª–∞ —ç—Ç–æ –¥–≤–µ –æ–ø–µ—Ä–∞—Ü–∏–∏ —É–∂–µ + –æ—Å—Ç–∞—Ç–æ–∫ –æ—Ç —Ü–µ–ª–æ—á–∏—Å–ª–µ–Ω–æ–≥–æ –¥–µ–ª–µ–Ω–∏—è –Ω–∞ –¥–≤–∞

	for left := 2; left < len(world); left++ { // –î–∞–ª–µ–µ –ø–æ –∫–∞–∂–¥–æ–º—É –ø—Ä–æ–µ—Ä—è–µ–º –±—ã–ª–∞ –ª–∏ –≤ –∫—ç—à–µ —É–∂–µ –ø–æ–¥–¥—Å—Ç—Ä–æ–∫–∞ –∏–ª–∏ –Ω–µ—Ç
		// –í–Ω—É—Ç—Ä–µ–Ω–Ω–∏–π —Ü–∏–∫–ª –ø—Ä–æ–≤–µ—Ä–∫–∏
		right := left + 2 // –ü—Ä–∞–≤—ã–π –º–∞—Ä–∫–µ—Ä
		if right > len(world)-1 {
			break
		}

		for {
			if chrs[world[left:right]] { // –ï—Å–ª–∏ —É–∂–µ –µ—Å—Ç—å, –∑–Ω–∞—á–∏—Ç –¥–≤–∏–≥–∞–µ–º –¥–∞–ª—å—à–µ
				right++ // –£–≤–µ–ª–∏—á–∏—ã–≤–∞–µ–º —à–∞–≥, –ø–æ–∫–∞ –Ω–µ –≤—Å—Ç—Ä–µ—Ç–∏–º –≤—Ö–æ–∂–¥–µ–Ω–∏—è
			} else {
				chrs[world[0:right]] = true // –ó–∞–ø–æ–º–Ω–∏–º —Ç–µ–∫—É—â—É—é –ø–æ–¥—Å—Ç—Ä–æ–∫—É –∏ —Å —Å–∞–º–æ–≥–æ –Ω–∞—á–∞–ª–∞
				chrs[world[left:right]] = true
				cnt++ // –£–≤–µ–ª–∏—á–∏–≤–∞–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –æ–ø–µ—Ä–∞—Ü–∏–π

				break
			}
		}
	}

	fmt.Println(cnt, chrs)
}
